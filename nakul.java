    public static void animate(String[][] frames) throws InterruptedException{
        
        while (true){
            for (String[] frame : frames){
                for (String line : frame){
                    // Adds some color!!!
                    line = line.replace("a", RED + "*" + RESET);
                    line = line.replace("b", BLUE + "*" + RESET);
                    line = line.replace("c", YELLOW + "*" + RESET);
                    System.out.println(line);
                }
                Thread.sleep(200);
                clearScreen();
            }
            // Animates Tie Fighter
            for(int i = 0; i < 50; i++){
                String spaces = new String(new char[i]).replace("\0", " ");
                for (String line : tieFighter){
                    System.out.println(spaces + line);
                }
                Thread.sleep(20);
                clearScreen();                
            }
        clearScreen();
        }
        
    }

    public static void fire() throws InterruptedException{
        int num = 0;
        while(num < 50){
            printSoldier(num);
            Thread.sleep(30);
            clearScreen();
            num++;
            if(num == 49){
                num = 0;
            }
        }
    }

